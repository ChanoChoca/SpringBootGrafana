package com.chanochoca.app.loans.entity;

/**
 * La enumeración `Currency` representa las posibles monedas en las que un préstamo puede ser denominados.
 *
 * Esta enumeración incluye las siguientes monedas:
 *
 * - `INR`: Rupia India.
 * - `USD`: Dólar Estadounidense.
 * - `EUR`: Euro.
 */
public enum Currency {
    /** Rupia India. */
    INR,

    /** Dólar Estadounidense. */
    USD,

    /** Euro. */
    EUR
}
